import UIKit

extension UIColor {
    static var tortilla: UIColor {
        return UIColor(red: 153 / 255, green: 121 / 255, blue: 80 / 255, alpha: 1)
    }
    static var lightTortilla: UIColor {
        return UIColor(red: 215 / 255, green: 198 / 255, blue: 176 / 255, alpha: 1)
    }
    static var darkOliveGreen: UIColor {
        return UIColor(red: 85 / 255, green: 107 / 255, blue: 47 / 255, alpha: 1)
    }
}
